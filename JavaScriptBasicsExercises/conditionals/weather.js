let weather = 'snowy';

switch (weather) {
  case 'sunny':
    console.log("It's a beautiful day!");
    break;
  case 'rainy':
    console.log('Grab your umbrella.');
    break;
  case 'snowy':
    console.log("Brrr! Let's drink some hot chocolate.");
    break;
  case 'raining frogs':
    console.log('Hmmm... Something is wrong.');
    break;
  default:
    console.log("Let's stay inside.");
}